CREATE DATABASE ORG;
USE ORG;

CREATE TABLE Worker (
WORKER_ID INT NOT NULL PRIMARY KEY IDENTITY(1,1),
FIRST_NAME CHAR (50),
LAST_NAME CHAR (50),
SALARY FLOAT (15),
JOINING_DATE DATE,
DEPARTMENT CHAR (25)
);

